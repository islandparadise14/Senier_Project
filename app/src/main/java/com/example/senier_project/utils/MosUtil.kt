package com.example.senier_project.utils

import android.content.Context

fun Char.koToMosNumber(): String =
    when (this) {
        'ㄱ' -> { "1211" }
        'ㄴ' -> { "1121" }
        'ㄷ' -> { "2111" }
        'ㄹ' -> { "1112" }
        'ㅁ' -> { "22" }
        'ㅂ' -> { "122" }
        'ㅅ' -> { "221" }
        'ㅇ' -> { "212" }
        'ㅈ' -> { "1221" }
        'ㅊ' -> { "2121" }
        'ㅋ' -> { "2112" }
        'ㅌ' -> { "2211" }
        'ㅍ' -> { "222" }
        'ㅎ' -> { "1222" }
        'ㅏ' -> { "1" }
        'ㅑ' -> { "11" }
        'ㅓ' -> { "2" }
        'ㅕ' -> { "111" }
        'ㅗ' -> { "12" }
        'ㅛ' -> { "21" }
        'ㅜ' -> { "1111" }
        'ㅠ' -> { "121" }
        'ㅡ' -> { "211" }
        'ㅣ' -> { "112" }
        'ㅔ' -> { "2122" }
        'ㅐ' -> { "2212" }
        else -> { "0" }
    }

fun Char.enToMosNumber(): String =
    when (if(this in 'a'..'z') this - 32 else this) {
        'A' -> { "12" }
        'B' -> { "2111" }
        'C' -> { "2121" }
        'D' -> { "211" }
        'E' -> { "1" }
        'F' -> { "1121" }
        'G' -> { "221" }
        'H' -> { "1111" }
        'I' -> { "11" }
        'J' -> { "1222" }
        'K' -> { "212" }
        'L' -> { "1211" }
        'M' -> { "22" }
        'N' -> { "21" }
        'O' -> { "222" }
        'P' -> { "1221" }
        'Q' -> { "2212" }
        'R' -> { "121" }
        'S' -> { "111" }
        'T' -> { "2" }
        'U' -> { "112" }
        'V' -> { "1112" }
        'W' -> { "122" }
        'X' -> { "2112" }
        'Y' -> { "2122" }
        'Z' -> { "2211" }
        '1' -> { "12222" }
        '2' -> { "11222" }
        '3' -> { "11122" }
        '4' -> { "11112" }
        '5' -> { "11111" }
        '6' -> { "21111" }
        '7' -> { "22111" }
        '8' -> { "22211" }
        '9' -> { "22221" }
        '0' -> { "22222" }
        else -> { "0" }
    }

fun Char.mosToVibrate(context: Context, speed: Int) =
    when (this) {
        '1' -> { context.vibrateShort(speed) }
        '2' -> { context.vibrateLong(speed) }
        else -> {
            vibrateBlank()
        }
    }